Политика конфиденциальности NovaBoost

Дата обновления: $lastUpdated

1. Кто мы
NovaBoost — приложение для управления озвучкой и алёртами во время LIVE-эфиров.

2. Какие данные мы обрабатываем
В зависимости от того, как вы используете приложение, мы можем обрабатывать:
- Данные аккаунта: имя пользователя, пароль (в виде хеша на сервере), email (если указан).
- Настройки: голос/громкость, триггеры/алёрты, TikTok-username для подключения к LIVE.
- Файлы, которые вы загружаете: аватар и пользовательские звуки (если вы их добавляете).
- Технические данные: токен авторизации, сведения об ошибках/логах, необходимые для работы сервиса.

3. Для чего мы используем данные
- Для предоставления функционала приложения (авторизация, сохранение настроек, проигрывание TTS/звуков, подключение к LIVE).
- Для поддержки и диагностики (устранение сбоев, улучшение стабильности).
- Для обработки покупок/подписок через магазины (Google Play / App Store).

4. Передача третьим сторонам
- Платёжные операции обрабатываются магазинами (Google Play / Apple). Мы получаем только данные, необходимые для проверки статуса подписки.
- Подключение к TikTok выполняется по вашему username и требуется для работы LIVE-функций.

5. Хранение и удаление
- Мы храним данные столько, сколько нужно для работы сервиса.
- Вы можете удалить аккаунт в приложении: Профиль → Удалить аккаунт. После удаления аккаунта связанные данные удаляются (в пределах, необходимых для работы и соблюдения требований закона/магазинов).

6. Безопасность
Мы принимаем разумные меры для защиты данных (аутентификация по токену, хранение пароля в виде хеша).

7. Контакты
По вопросам конфиденциальности и удаления данных:
Email: support@novaboost.cloud
